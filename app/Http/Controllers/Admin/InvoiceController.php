    public function update($id,InvoiceRequest $request)
    {

        try {
            $invoice = Invoice::find($id);
            if (!$invoice) {
                return redirect()->route('admin.invoices.edit', $id)->with(['error' => 'هذه اللغة غير موجوده']);
            }


        

            $invoice->update($request->except('_token','id','product_name','sub_total','quantity','unite','unite_price'));

            $invoice->details()->delete();
            
         

            $details_list = [];
            for ($i = 0; $i < count($request->product_name); $i++) {
                $details_list[$i]['product_name'] = $request->product_name[$i];
                $details_list[$i]['unite'] = $request->unite[$i];
                $details_list[$i]['quantity'] = $request->quantity[$i];
                $details_list[$i]['unite_price'] = $request->unite_price[$i];
                $details_list[$i]['sub_total'] = $request->sub_total[$i];
            }
    
            $details = $invoice->details()->createMany($details_list);
    
            if ($details) {
                return redirect()->route('admin.invoices')->with(['success' => 'تم تحديث اللغة بنجاح']);
            }

        } catch (\Exception $ex) {
            return $ex;
            return redirect()->route('admin.invoices')->with(['error' => 'هناك خطا ما يرجي المحاوله فيما بعد']);
        }
    }
